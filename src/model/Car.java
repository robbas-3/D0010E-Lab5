package model;

/**
 * Class that represents the car.
 */
public class Car {

	private int id;

	/**
	 * Method for car with an id.
	 * 
	 * @param id
	 */
	public Car(int id) {
		this.id = id;
	}

	/**
	 * Method to return the private variable id of the car.
	 * 
	 * @return
	 */
	public int getId() {
		return id;
	}

}
